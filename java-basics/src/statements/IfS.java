package statements;

public class IfS {
    public static void main(String[] args) {
        int a = 9;
        if (a > 0) {
            System.out.println("It's Positive");
        } else if (a == 0) {
            System.out.println("It's 0");
        } else {
            System.out.println("It's Negative");
        }
    }
}
